import assert from 'node:assert/strict';
import { spawnSync } from 'node:child_process';
import fs from 'node:fs';
import os from 'node:os';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

import { createAnthropicProvider } from '../src/providers/anthropic-provider.mjs';

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(scriptDir, '..');
const tempRoot = fs.mkdtempSync(path.join(os.tmpdir(), 'personal-ai-agent-anthropic-provider-'));
const workspacePath = path.join(tempRoot, 'workspace');
const cliPath = path.join(repoRoot, 'src', 'cli.mjs');

fs.mkdirSync(workspacePath, { recursive: true });

function runCli({ args, env = {} }) {
  const result = spawnSync(process.execPath, [cliPath, ...args], {
    cwd: repoRoot,
    encoding: 'utf8',
    env: {
      ...process.env,
      ...env,
      ANTHROPIC_API_KEY: env.ANTHROPIC_API_KEY || '',
      PERSONAL_AI_AGENT_ROOT: tempRoot,
    },
  });

  return {
    status: result.status,
    stderr: String(result.stderr || ''),
    stdout: String(result.stdout || ''),
  };
}

const workspaceCreate = runCli({
  args: ['workspace', 'add', workspacePath, '--name', 'anthropic-provider-workspace'],
});
assert.equal(workspaceCreate.status, 0);
const workspace = JSON.parse(workspaceCreate.stdout);

const missionCreate = runCli({
  args: [
    'mission',
    'create',
    '--workspace',
    workspace.id,
    '--mode',
    'knowledge',
    '--deliverable',
    'decision-memo',
    '--title',
    'Anthropic provider env validation',
    '--objective',
    'Verify provider wiring and configuration handling.',
  ],
});
assert.equal(missionCreate.status, 0);
const mission = JSON.parse(missionCreate.stdout);

const missingKeyRun = runCli({
  args: ['mission', 'run', mission.id, '--provider', 'anthropic'],
  env: {
    ANTHROPIC_API_KEY: '',
  },
});

assert.equal(missingKeyRun.status, 0);
assert.equal(missingKeyRun.stderr, '');
const missingKeyResult = JSON.parse(missingKeyRun.stdout);
assert.equal(missingKeyResult.missionId, mission.id);
assert.equal(missingKeyResult.provider, 'anthropic');
assert.equal(missingKeyResult.status, 'failed');
assert.equal(missingKeyResult.artifactPath, null);
assert.equal(missingKeyResult.reviewerVerdict, null);

const failedActivityRun = runCli({
  args: ['provider', 'activity', '--provider', 'anthropic', '--status', 'failed'],
});

assert.equal(failedActivityRun.status, 0);
const failedActivity = JSON.parse(failedActivityRun.stdout);
assert.equal(failedActivity.executions.length, 1);
assert.equal(failedActivity.executions[0].providerId, 'anthropic');
assert.equal(failedActivity.executions[0].role, 'manager');
assert.equal(failedActivity.executions[0].status, 'failed');
assert.equal(failedActivity.executions[0].failureKind, 'config');
assert.equal(failedActivity.executions[0].attemptCount, 1);
assert.equal(failedActivity.executions[0].recoverable, false);
assert.equal(failedActivity.executions[0].timedOut, false);

let capturedRequest = null;
const provider = createAnthropicProvider({
  rootDir: repoRoot,
  env: {
    ANTHROPIC_API_KEY: 'test-key',
    ANTHROPIC_BASE_URL: 'https://api.anthropic.test/v1',
    ANTHROPIC_MODEL: 'claude-sonnet-4-6',
    ANTHROPIC_VERSION: '2023-06-01',
    ANTHROPIC_MAX_TOKENS: '2048',
  },
  fetchImpl: async (url, init) => {
    capturedRequest = {
      body: JSON.parse(init.body),
      headers: init.headers,
      method: init.method,
      url,
    };

    return {
      ok: true,
      status: 200,
      async json() {
        return {
          content: [
            {
              text: JSON.stringify({
                artifactContent:
                  '# Manager Context\n\n## Mission\n- title: Anthropic provider smoke\n\n## Objective\nValidate the adapter.\n\n## Relevant Memory\n- no relevant memory found\n\n## Governance\n- approval likely: no\n- risk reason: none\n',
                summaryText: 'Manager context generated by mocked Anthropic provider.',
              }),
              type: 'text',
            },
          ],
          id: 'msg_test',
        };
      },
    };
  },
});

const providerInput = {
  role: 'manager',
  mission: {
    constraints: [],
    deliverableType: 'decision-memo',
    id: 'mission_test',
    mode: 'knowledge',
    objective: 'Validate the adapter.',
    title: 'Anthropic provider smoke',
  },
  workspace: {
    id: 'workspace_test',
    name: 'Smoke Workspace',
    path: workspacePath,
  },
  pack: {
    artifactFileName: 'decision-memo.md',
    artifactTitle: 'Decision Memo',
    plannerGuidance: ['Keep the scope bounded.'],
    requiredSections: ['Context', 'Options', 'Recommendation', 'Why This Path', 'Next Action'],
    reviewRules: [],
    riskProfile: {
      actionKind: 'document-draft',
      reason: 'none',
      requiresApproval: false,
      title: 'No approval required',
    },
  },
  memoryEntries: [],
  previousOutputs: {},
};

const rawOutput = await provider.run(providerInput);
const normalizedOutput = provider.normalizeOutput(rawOutput, providerInput);

assert.equal(capturedRequest.method, 'POST');
assert.equal(capturedRequest.url, 'https://api.anthropic.test/v1/messages');
assert.equal(capturedRequest.body.model, 'claude-sonnet-4-6');
assert.equal(capturedRequest.body.max_tokens, 2048);
assert.equal(capturedRequest.body.messages[0].role, 'user');
assert.match(capturedRequest.body.messages[0].content, /Structured Output Contract/);
assert.match(capturedRequest.body.system, /Return only valid JSON/i);
assert.equal(capturedRequest.headers['x-api-key'], 'test-key');
assert.equal(capturedRequest.headers['anthropic-version'], '2023-06-01');
assert.equal(normalizedOutput.type, 'manager');
assert.equal(normalizedOutput.artifactFileName, 'manager-context.md');
assert.equal(normalizedOutput.artifactTitle, 'Manager Context');
assert.equal(normalizedOutput.summaryText, 'Manager context generated by mocked Anthropic provider.');
assert.match(normalizedOutput.artifactContent, /# Manager Context/);

console.log(
  JSON.stringify(
    {
      ok: true,
      mode: 'anthropic-provider',
      requestModel: capturedRequest.body.model,
      validatedMissingKeyPath: true,
    },
    null,
    2,
  ),
);
