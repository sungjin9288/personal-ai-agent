import assert from 'node:assert/strict';
import fs from 'node:fs';
import os from 'node:os';
import path from 'node:path';

import { createMissionService } from '../src/core/mission-service.mjs';
import { createStore } from '../src/core/store.mjs';

const tempRoot = fs.mkdtempSync(path.join(os.tmpdir(), 'personal-ai-agent-provider-cost-telemetry-'));
const workspacePath = path.join(tempRoot, 'workspace');

fs.mkdirSync(workspacePath, { recursive: true });

function roundUsd(value) {
  return Number(Number(value).toFixed(8));
}

function estimateCostUsd({ inputTokens, outputTokens, inputRate, outputRate }) {
  return roundUsd(Number(inputTokens) * inputRate / 1_000_000 + Number(outputTokens) * outputRate / 1_000_000);
}

const originalFetch = globalThis.fetch;
const originalEnv = {
  LOCAL_PROVIDER_API_KEY: process.env.LOCAL_PROVIDER_API_KEY,
  LOCAL_PROVIDER_BASE_URL: process.env.LOCAL_PROVIDER_BASE_URL,
  LOCAL_PROVIDER_MODEL: process.env.LOCAL_PROVIDER_MODEL,
  LOCAL_INPUT_COST_PER_1M_USD: process.env.LOCAL_INPUT_COST_PER_1M_USD,
  LOCAL_OUTPUT_COST_PER_1M_USD: process.env.LOCAL_OUTPUT_COST_PER_1M_USD,
};

const inputRate = 1.25;
const outputRate = 5;

let localProbeCount = 0;
let localRunCount = 0;

const successfulStageUsage = [
  { input: 120, output: 80, total: 200 },
  { input: 140, output: 100, total: 240 },
  { input: 200, output: 160, total: 360 },
  { input: 90, output: 45, total: 135 },
];

const successfulStageOutputs = [
  {
    artifactContent:
      '# Manager Context\n\n## Mission\n- title: Provider cost success mission\n- workspace: provider-cost-workspace\n- mode: knowledge\n- deliverable: decision-memo\n\n## Objective\nValidate provider cost telemetry propagation.\n\n## Relevant Memory\n- no relevant memory found\n\n## Governance\n- approval likely: no\n- risk reason: none\n',
    summaryText: 'Manager context generated by local provider.',
  },
  {
    adaptationNotes: [],
    artifactContent:
      '# Planner Plan\n\n## Mission\n- title: Provider cost success mission\n- workspace: provider-cost-workspace\n- deliverable: decision-memo\n\n## Plan\n1. Draft the decision memo.\n\n## Adaptation Signals\n- No prior mission memory influenced this plan.\n\n## Verification Lens\n- preserve required sections exactly once\n',
    planSteps: ['Draft the decision memo.'],
    summaryText: 'Planner plan generated by local provider.',
  },
  {
    adaptationNotes: [],
    artifactContent:
      '# Decision Memo\n\n## Context\nProvider cost telemetry context.\n\n## Options\n- Keep execution usage bounded.\n\n## Recommendation\nPersist estimated provider cost alongside execution usage.\n\n## Why This Path\nIt makes execution spend auditable in the same read-models.\n\n## Next Action\nReview the execution cost summary.\n',
    nextAction: 'Review the execution cost summary.',
    summaryText: 'Decision memo generated by local provider.',
  },
  {
    artifactContent:
      '# Reviewer Report\n\n## Verdict\n- verdict: pass\n\n## Checks\n- pass: content-sections - Decision memo sections are present.\n\n## Findings\n- No findings. The draft preserves required sections and includes a next action.\n\n## Next Action\n- continue to completion or approval gate\n',
    checks: [
      {
        description: 'Decision memo sections are present.',
        id: 'content-sections',
        passed: true,
      },
    ],
    findings: [],
    summaryText: 'Reviewer accepted the local provider draft.',
    verdict: 'pass',
  },
];

const failedManagerUsage = { input: 60, output: 20, total: 80 };

try {
  process.env.LOCAL_PROVIDER_API_KEY = 'test-local-key';
  process.env.LOCAL_PROVIDER_BASE_URL = 'http://127.0.0.1:1234/v1';
  process.env.LOCAL_PROVIDER_MODEL = 'llama3.1-local';
  process.env.LOCAL_INPUT_COST_PER_1M_USD = String(inputRate);
  process.env.LOCAL_OUTPUT_COST_PER_1M_USD = String(outputRate);

  globalThis.fetch = async (url, init = {}) => {
    if (url === 'http://127.0.0.1:1234/v1/models') {
      localProbeCount += 1;
      return {
        ok: true,
        status: 200,
        async json() {
          return {
            data: [{ id: 'llama3.1-local' }, { id: 'qwen3-local' }],
          };
        },
      };
    }

    if (url === 'http://127.0.0.1:1234/v1/chat/completions') {
      assert.equal(init.method, 'POST');
      localRunCount += 1;

      if (localRunCount <= successfulStageOutputs.length) {
        const stageUsage = successfulStageUsage[localRunCount - 1];
        const output = successfulStageOutputs[localRunCount - 1];

        return {
          ok: true,
          status: 200,
          async json() {
            return {
              choices: [
                {
                  message: {
                    content: JSON.stringify(output),
                    role: 'assistant',
                  },
                },
              ],
              id: `chatcmpl_provider_cost_success_${localRunCount}`,
              usage: {
                completion_tokens: stageUsage.output,
                prompt_tokens: stageUsage.input,
                total_tokens: stageUsage.total,
              },
            };
          },
        };
      }

      return {
        ok: true,
        status: 200,
        async json() {
          return {
            choices: [
              {
                message: {
                  content: 'not-json-response',
                  role: 'assistant',
                },
              },
            ],
            id: 'chatcmpl_provider_cost_failed_manager',
            usage: {
              completion_tokens: failedManagerUsage.output,
              prompt_tokens: failedManagerUsage.input,
              total_tokens: failedManagerUsage.total,
            },
          };
        },
      };
    }

    throw new Error(`Unexpected provider cost telemetry url: ${url}`);
  };

  const store = createStore({ rootDir: tempRoot });
  const service = createMissionService({ store, rootDir: tempRoot });

  const workspace = service.addWorkspace({
    name: 'provider-cost-workspace',
    workspacePath,
  });

  const probeResult = await service.probeProvider('local');
  assert.equal(localProbeCount, 1);
  assert.equal(probeResult.ok, true);

  const successMission = service.createMission({
    workspaceId: workspace.id,
    mode: 'knowledge',
    deliverableType: 'decision-memo',
    title: 'Provider cost success mission',
    objective: 'Validate successful provider cost telemetry propagation.',
    constraints: [],
  });

  const successRun = await service.runMission(successMission.id, {
    provider: 'local',
    providerSpecified: true,
  });
  assert.equal(successRun.mission.status, 'completed');

  const failedMission = service.createMission({
    workspaceId: workspace.id,
    mode: 'knowledge',
    deliverableType: 'decision-memo',
    title: 'Provider cost failed mission',
    objective: 'Validate failed execution provider cost propagation.',
    constraints: [],
  });

  const failedRun = await service.runMission(failedMission.id, {
    provider: 'local',
    providerSpecified: true,
  });
  assert.equal(failedRun.mission.status, 'failed');
  assert.equal(localRunCount, 5);

  const successfulCosts = successfulStageUsage.map((usage) =>
    estimateCostUsd({
      inputTokens: usage.input,
      outputTokens: usage.output,
      inputRate,
      outputRate,
    }),
  );
  const failedManagerCost = estimateCostUsd({
    inputTokens: failedManagerUsage.input,
    outputTokens: failedManagerUsage.output,
    inputRate,
    outputRate,
  });
  const successfulCostTotal = roundUsd(successfulCosts.reduce((sum, value) => sum + value, 0));
  const combinedCostTotal = roundUsd(successfulCostTotal + failedManagerCost);
  const combinedCostAverage = roundUsd(combinedCostTotal / 5);
  const combinedCostMax = Math.max(...successfulCosts, failedManagerCost);
  const combinedCostByProviderId = {
    local: combinedCostTotal,
  };
  const combinedCostByRole = {
    executor: successfulCosts[2],
    manager: roundUsd(successfulCosts[0] + failedManagerCost),
    planner: successfulCosts[1],
    reviewer: successfulCosts[3],
  };

  const providerCheck = service.checkProvider('local');
  assert.equal(providerCheck.configuration.inputCostPer1MUsd, '1.25');
  assert.equal(providerCheck.configuration.outputCostPer1MUsd, '5');
  assert.equal(providerCheck.latestExecution.status, 'failed');
  assert.equal(providerCheck.latestExecution.estimatedCostUsd, failedManagerCost);
  assert.equal(providerCheck.latestExecution.usageInputTokens, failedManagerUsage.input);
  assert.equal(providerCheck.latestExecution.usageOutputTokens, failedManagerUsage.output);
  assert.equal(providerCheck.latestExecution.usageTotalTokens, failedManagerUsage.total);
  assert.equal(providerCheck.latestPendingAttention.estimatedCostUsd, failedManagerCost);

  const providerActivity = service.getProviderExecutionHistory({ providerId: 'local' });
  assert.equal(providerActivity.executions.length, 5);
  assert.equal(providerActivity.summary.estimatedCostUsdTotal, combinedCostTotal);
  assert.equal(providerActivity.summary.estimatedCostUsdAverage, combinedCostAverage);
  assert.equal(providerActivity.summary.estimatedCostUsdMax, combinedCostMax);
  assert.equal(providerActivity.summary.estimatedCostUsdPricedCount, 5);
  assert.deepEqual(providerActivity.summary.estimatedCostUsdByProviderId, combinedCostByProviderId);
  assert.deepEqual(providerActivity.summary.estimatedCostUsdByRole, combinedCostByRole);

  const providerExecutionTimeline = service.getProviderExecutionTimeline({ providerId: 'local' });
  assert.equal(providerExecutionTimeline.summary.estimatedCostUsdTotal, combinedCostTotal);
  assert.deepEqual(providerExecutionTimeline.summary.estimatedCostUsdByProviderId, combinedCostByProviderId);
  assert.deepEqual(providerExecutionTimeline.summary.estimatedCostUsdByRole, combinedCostByRole);
  assert.equal(providerExecutionTimeline.timeline.at(-1).estimatedCostUsd, failedManagerCost);

  const providerEvents = service.getProviderEventTimeline({ providerId: 'local' });
  assert.equal(providerEvents.summary.executionEstimatedCostUsdTotal, combinedCostTotal);
  assert.equal(providerEvents.summary.executionEstimatedCostUsdAverage, combinedCostAverage);
  assert.equal(providerEvents.summary.executionEstimatedCostUsdMax, combinedCostMax);
  assert.equal(providerEvents.summary.executionEstimatedCostUsdPricedCount, 5);
  assert.deepEqual(providerEvents.summary.executionEstimatedCostUsdByProviderId, combinedCostByProviderId);
  assert.deepEqual(providerEvents.summary.executionEstimatedCostUsdByRole, combinedCostByRole);

  const providerOverview = service.getProviderOverview();
  assert.equal(providerOverview.summary.executionEstimatedCostUsdTotal, combinedCostTotal);
  assert.equal(providerOverview.summary.executionEstimatedCostUsdAverage, combinedCostAverage);
  assert.equal(providerOverview.summary.executionEstimatedCostUsdMax, combinedCostMax);
  assert.equal(providerOverview.summary.executionEstimatedCostUsdPricedCount, 5);
  assert.deepEqual(providerOverview.summary.executionEstimatedCostUsdByProviderId, combinedCostByProviderId);
  assert.deepEqual(providerOverview.summary.executionEstimatedCostUsdByRole, combinedCostByRole);

  const successMissionSummary = service.showMission(successMission.id).summary;
  assert.equal(successMissionSummary.providerExecutionEstimatedCostUsdTotal, successfulCostTotal);
  assert.equal(successMissionSummary.providerExecutionEstimatedCostUsdPricedCount, 4);
  assert.deepEqual(successMissionSummary.providerExecutionEstimatedCostUsdByProviderId, {
    local: successfulCostTotal,
  });
  assert.deepEqual(successMissionSummary.providerExecutionEstimatedCostUsdByRole, {
    executor: successfulCosts[2],
    manager: successfulCosts[0],
    planner: successfulCosts[1],
    reviewer: successfulCosts[3],
  });

  const failedMissionTimeline = service.getMissionTimeline(failedMission.id);
  assert.equal(failedMissionTimeline.summary.providerExecutionEstimatedCostUsdTotal, failedManagerCost);
  assert.equal(
    failedMissionTimeline.timeline.some(
      (event) => event.kind === 'provider-execution-failed' && event.estimatedCostUsd === failedManagerCost,
    ),
    true,
  );

  const workspaceOverview = service.getWorkspaceOverview(workspace.id);
  assert.equal(workspaceOverview.summary.providerExecutionEstimatedCostUsdTotal, combinedCostTotal);
  assert.equal(workspaceOverview.summary.providerExecutionEstimatedCostUsdAverage, combinedCostAverage);
  assert.equal(workspaceOverview.summary.providerExecutionEstimatedCostUsdMax, combinedCostMax);
  assert.equal(workspaceOverview.summary.providerExecutionEstimatedCostUsdPricedCount, 5);
  assert.deepEqual(workspaceOverview.summary.providerExecutionEstimatedCostUsdByProviderId, combinedCostByProviderId);
  assert.deepEqual(workspaceOverview.summary.providerExecutionEstimatedCostUsdByRole, combinedCostByRole);

  const globalOverview = service.getGlobalOverview();
  assert.equal(globalOverview.summary.providerExecutionEstimatedCostUsdTotal, combinedCostTotal);
  assert.equal(globalOverview.summary.providerExecutionEstimatedCostUsdAverage, combinedCostAverage);
  assert.equal(globalOverview.summary.providerExecutionEstimatedCostUsdMax, combinedCostMax);
  assert.equal(globalOverview.summary.providerExecutionEstimatedCostUsdPricedCount, 5);
  assert.deepEqual(globalOverview.summary.providerExecutionEstimatedCostUsdByProviderId, combinedCostByProviderId);
  assert.deepEqual(globalOverview.summary.providerExecutionEstimatedCostUsdByRole, combinedCostByRole);
} finally {
  globalThis.fetch = originalFetch;

  for (const [key, value] of Object.entries(originalEnv)) {
    if (value === undefined) {
      delete process.env[key];
    } else {
      process.env[key] = value;
    }
  }
}

console.log(
  JSON.stringify(
    {
      mode: 'provider-cost-telemetry',
      ok: true,
      providerId: 'local',
    },
    null,
    2,
  ),
);
