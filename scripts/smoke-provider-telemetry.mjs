import assert from 'node:assert/strict';
import fs from 'node:fs';
import os from 'node:os';
import path from 'node:path';

import { createMissionService } from '../src/core/mission-service.mjs';
import { createStore } from '../src/core/store.mjs';

const tempRoot = fs.mkdtempSync(path.join(os.tmpdir(), 'personal-ai-agent-provider-telemetry-'));
const workspacePath = path.join(tempRoot, 'workspace');

fs.mkdirSync(workspacePath, { recursive: true });

function sleep(ms) {
  return new Promise((resolve) => setTimeout(resolve, ms));
}

const originalFetch = globalThis.fetch;
const originalEnv = {
  LOCAL_PROVIDER_API_KEY: process.env.LOCAL_PROVIDER_API_KEY,
  LOCAL_PROVIDER_BASE_URL: process.env.LOCAL_PROVIDER_BASE_URL,
  LOCAL_PROVIDER_MODEL: process.env.LOCAL_PROVIDER_MODEL,
};

let localProbeCount = 0;
let localRunCount = 0;

const usageByStage = [
  { input: 120, output: 80, total: 200 },
  { input: 140, output: 100, total: 240 },
  { input: 200, output: 160, total: 360 },
  { input: 90, output: 45, total: 135 },
];

try {
  process.env.LOCAL_PROVIDER_API_KEY = 'test-local-key';
  process.env.LOCAL_PROVIDER_BASE_URL = 'http://127.0.0.1:1234/v1';
  process.env.LOCAL_PROVIDER_MODEL = 'llama3.1-local';

  globalThis.fetch = async (url, init = {}) => {
    if (url === 'http://127.0.0.1:1234/v1/models') {
      localProbeCount += 1;
      await sleep(5);
      return {
        ok: true,
        status: 200,
        async json() {
          return {
            data: [{ id: 'llama3.1-local' }, { id: 'qwen3-local' }],
          };
        },
      };
    }

    if (url === 'http://127.0.0.1:1234/v1/chat/completions') {
      assert.equal(init.method, 'POST');
      localRunCount += 1;
      await sleep(5);

      const outputs = [
        {
          artifactContent:
            '# Manager Context\n\n## Mission\n- title: Provider telemetry mission\n- workspace: provider-telemetry-workspace\n- mode: knowledge\n- deliverable: decision-memo\n\n## Objective\nValidate provider telemetry propagation.\n\n## Relevant Memory\n- no relevant memory found\n\n## Governance\n- approval likely: no\n- risk reason: none\n',
          summaryText: 'Manager context generated by local provider.',
        },
        {
          adaptationNotes: [],
          artifactContent:
            '# Planner Plan\n\n## Mission\n- title: Provider telemetry mission\n- workspace: provider-telemetry-workspace\n- deliverable: decision-memo\n\n## Plan\n1. Draft the decision memo.\n\n## Adaptation Signals\n- No prior mission memory influenced this plan.\n\n## Verification Lens\n- preserve required sections exactly once\n',
          planSteps: ['Draft the decision memo.'],
          summaryText: 'Planner plan generated by local provider.',
        },
        {
          adaptationNotes: [],
          artifactContent:
            '# Decision Memo\n\n## Context\nProvider telemetry context.\n\n## Options\n- Keep the draft path bounded.\n\n## Recommendation\nUse telemetry-aware provider surfaces.\n\n## Why This Path\nIt validates latency and token accounting propagation.\n\n## Next Action\nShare the memo with the owner.\n',
          nextAction: 'Share the memo with the owner.',
          summaryText: 'Decision memo generated by local provider.',
        },
        {
          artifactContent:
            '# Reviewer Report\n\n## Verdict\n- verdict: pass\n\n## Checks\n- pass: content-sections - Decision memo sections are present.\n\n## Findings\n- No findings. The draft preserves required sections and includes a next action.\n\n## Next Action\n- continue to completion or approval gate\n',
          checks: [
            {
              description: 'Decision memo sections are present.',
              id: 'content-sections',
              passed: true,
            },
          ],
          findings: [],
          summaryText: 'Reviewer accepted the local provider draft.',
          verdict: 'pass',
        },
      ];

      const usage = usageByStage[localRunCount - 1];

      return {
        ok: true,
        status: 200,
        async json() {
          return {
            choices: [
              {
                message: {
                  content: JSON.stringify(outputs[localRunCount - 1]),
                  role: 'assistant',
                },
              },
            ],
            id: `chatcmpl_local_provider_telemetry_${localRunCount}`,
            usage: {
              completion_tokens: usage.output,
              prompt_tokens: usage.input,
              total_tokens: usage.total,
            },
          };
        },
      };
    }

    throw new Error(`Unexpected provider telemetry url: ${url}`);
  };

  const store = createStore({ rootDir: tempRoot });
  const service = createMissionService({ store, rootDir: tempRoot });

  const workspace = service.addWorkspace({
    name: 'provider-telemetry-workspace',
    workspacePath,
  });

  const probeResult = await service.probeProvider('local');
  assert.equal(localProbeCount, 1);
  assert.ok(Number(probeResult.durationMs) > 0);

  const mission = service.createMission({
    workspaceId: workspace.id,
    mode: 'knowledge',
    deliverableType: 'decision-memo',
    title: 'Provider telemetry mission',
    objective: 'Validate provider telemetry propagation.',
    constraints: [],
  });

  const runResult = await service.runMission(mission.id, {
    provider: 'local',
    providerSpecified: true,
  });

  assert.equal(runResult.mission.status, 'completed');
  assert.equal(localRunCount, 4);

  const expectedInputTokensTotal = usageByStage.reduce((sum, item) => sum + item.input, 0);
  const expectedOutputTokensTotal = usageByStage.reduce((sum, item) => sum + item.output, 0);
  const expectedTotalTokensTotal = usageByStage.reduce((sum, item) => sum + item.total, 0);

  const providerCheck = service.checkProvider('local');
  assert.ok(Number(providerCheck.latestProbe.durationMs) > 0);
  assert.ok(Number(providerCheck.latestExecution.durationMs) > 0);
  assert.equal(providerCheck.latestExecution.usageInputTokens, usageByStage.at(-1).input);
  assert.equal(providerCheck.latestExecution.usageOutputTokens, usageByStage.at(-1).output);
  assert.equal(providerCheck.latestExecution.usageTotalTokens, usageByStage.at(-1).total);

  const providerHistory = service.listProviderProbeHistory({ providerId: 'local' });
  assert.equal(providerHistory.probes.length, 1);
  assert.ok(Number(providerHistory.summary.totalDurationMs) >= Number(probeResult.durationMs));
  assert.ok(Number(providerHistory.summary.averageDurationMs) > 0);

  const providerActivity = service.getProviderExecutionHistory({ providerId: 'local' });
  assert.equal(providerActivity.executions.length, 4);
  assert.ok(Number(providerActivity.summary.totalDurationMs) > 0);
  assert.ok(Number(providerActivity.summary.averageDurationMs) > 0);
  assert.equal(providerActivity.summary.usageInputTokensTotal, expectedInputTokensTotal);
  assert.equal(providerActivity.summary.usageOutputTokensTotal, expectedOutputTokensTotal);
  assert.equal(providerActivity.summary.usageTotalTokensTotal, expectedTotalTokensTotal);

  const providerEvents = service.getProviderEventTimeline({ providerId: 'local' });
  assert.ok(Number(providerEvents.summary.probeTotalDurationMs) >= Number(probeResult.durationMs));
  assert.ok(Number(providerEvents.summary.executionTotalDurationMs) > 0);
  assert.equal(providerEvents.summary.usageInputTokensTotal, expectedInputTokensTotal);
  assert.equal(providerEvents.summary.usageOutputTokensTotal, expectedOutputTokensTotal);
  assert.equal(providerEvents.summary.usageTotalTokensTotal, expectedTotalTokensTotal);

  const providerOverview = service.getProviderOverview();
  assert.ok(Number(providerOverview.summary.probeTotalDurationMs) >= Number(probeResult.durationMs));
  assert.ok(Number(providerOverview.summary.executionTotalDurationMs) > 0);
  assert.equal(providerOverview.summary.usageInputTokensTotal, expectedInputTokensTotal);
  assert.equal(providerOverview.summary.usageOutputTokensTotal, expectedOutputTokensTotal);
  assert.equal(providerOverview.summary.usageTotalTokensTotal, expectedTotalTokensTotal);

  const missionSummary = service.showMission(mission.id).summary;
  assert.ok(Number(missionSummary.providerExecutionTotalDurationMs) > 0);
  assert.equal(missionSummary.providerExecutionUsageInputTokensTotal, expectedInputTokensTotal);
  assert.equal(missionSummary.providerExecutionUsageOutputTokensTotal, expectedOutputTokensTotal);
  assert.equal(missionSummary.providerExecutionUsageTotalTokensTotal, expectedTotalTokensTotal);

  const workspaceOverview = service.getWorkspaceOverview(workspace.id);
  assert.ok(Number(workspaceOverview.summary.providerExecutionTotalDurationMs) > 0);
  assert.equal(workspaceOverview.summary.providerExecutionUsageInputTokensTotal, expectedInputTokensTotal);
  assert.equal(workspaceOverview.summary.providerExecutionUsageOutputTokensTotal, expectedOutputTokensTotal);
  assert.equal(workspaceOverview.summary.providerExecutionUsageTotalTokensTotal, expectedTotalTokensTotal);

  const globalOverview = service.getGlobalOverview();
  assert.ok(Number(globalOverview.summary.providerExecutionTotalDurationMs) > 0);
  assert.ok(Number(globalOverview.summary.providerProbeTotalDurationMs) >= Number(probeResult.durationMs));
  assert.equal(globalOverview.summary.providerExecutionUsageInputTokensTotal, expectedInputTokensTotal);
  assert.equal(globalOverview.summary.providerExecutionUsageOutputTokensTotal, expectedOutputTokensTotal);
  assert.equal(globalOverview.summary.providerExecutionUsageTotalTokensTotal, expectedTotalTokensTotal);
} finally {
  globalThis.fetch = originalFetch;

  for (const [key, value] of Object.entries(originalEnv)) {
    if (value === undefined) {
      delete process.env[key];
    } else {
      process.env[key] = value;
    }
  }
}

console.log(
  JSON.stringify(
    {
      mode: 'provider-telemetry',
      ok: true,
      providerId: 'local',
    },
    null,
    2,
  ),
);
