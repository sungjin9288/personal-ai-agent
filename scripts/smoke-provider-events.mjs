import assert from 'node:assert/strict';
import { spawnSync } from 'node:child_process';
import fs from 'node:fs';
import os from 'node:os';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

import { createMissionService } from '../src/core/mission-service.mjs';
import { createStore } from '../src/core/store.mjs';

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(scriptDir, '..');
const cliPath = path.join(repoRoot, 'src', 'cli.mjs');
const tempRoot = fs.mkdtempSync(path.join(os.tmpdir(), 'personal-ai-agent-provider-events-'));
const workspacePath = path.join(tempRoot, 'workspace');

fs.mkdirSync(workspacePath, { recursive: true });

function runCli({ args, env = {} }) {
  const result = spawnSync(process.execPath, [cliPath, ...args], {
    cwd: repoRoot,
    encoding: 'utf8',
    env: {
      ...process.env,
      ...env,
      PERSONAL_AI_AGENT_ROOT: tempRoot,
    },
  });

  return {
    status: result.status,
    stderr: String(result.stderr || ''),
    stdout: String(result.stdout || ''),
  };
}

const missingOpenAIProbeResult = runCli({
  args: ['provider', 'probe', 'openai'],
  env: {
    OPENAI_API_KEY: '',
  },
});

assert.equal(missingOpenAIProbeResult.status, 0);
const missingOpenAIProbe = JSON.parse(missingOpenAIProbeResult.stdout);
assert.equal(missingOpenAIProbe.id, 'openai');
assert.equal(missingOpenAIProbe.attempted, false);
assert.equal(missingOpenAIProbe.ok, false);

const originalFetch = globalThis.fetch;
const originalEnv = {
  LOCAL_PROVIDER_API_KEY: process.env.LOCAL_PROVIDER_API_KEY,
  LOCAL_PROVIDER_BASE_URL: process.env.LOCAL_PROVIDER_BASE_URL,
  LOCAL_PROVIDER_MODEL: process.env.LOCAL_PROVIDER_MODEL,
};

let localChatRequestCount = 0;

try {
  process.env.LOCAL_PROVIDER_API_KEY = 'test-local-key';
  process.env.LOCAL_PROVIDER_BASE_URL = 'http://127.0.0.1:1234/v1';
  process.env.LOCAL_PROVIDER_MODEL = 'llama3.1-local';

  globalThis.fetch = async (url, init = {}) => {
    if (url === 'http://127.0.0.1:1234/v1/models') {
      return {
        ok: true,
        status: 200,
        async json() {
          return {
            data: [{ id: 'llama3.1-local' }, { id: 'qwen3-local' }],
          };
        },
      };
    }

    if (url === 'http://127.0.0.1:1234/v1/chat/completions') {
      assert.equal(init.method, 'POST');
      localChatRequestCount += 1;

      const outputs = [
        {
          artifactContent:
            '# Manager Context\n\n## Mission\n- title: Local provider events mission\n- workspace: provider-events-workspace\n- mode: knowledge\n- deliverable: decision-memo\n\n## Objective\nValidate unified provider events.\n\n## Relevant Memory\n- no relevant memory found\n\n## Governance\n- approval likely: no\n- risk reason: none\n',
          summaryText: 'Manager context generated by local provider.',
        },
        {
          adaptationNotes: [],
          artifactContent:
            '# Planner Plan\n\n## Mission\n- title: Local provider events mission\n- workspace: provider-events-workspace\n- deliverable: decision-memo\n\n## Plan\n1. Draft the decision memo.\n\n## Adaptation Signals\n- No prior mission memory influenced this plan.\n\n## Verification Lens\n- preserve required sections exactly once\n',
          planSteps: ['Draft the decision memo.'],
          summaryText: 'Planner plan generated by local provider.',
        },
        {
          adaptationNotes: [],
          artifactContent:
            '# Decision Memo\n\n## Context\nLocal provider event context.\n\n## Options\n- Keep the draft path bounded.\n\n## Recommendation\nUse the local provider path.\n\n## Why This Path\nIt validates unified provider observability.\n\n## Next Action\nShare the memo with the owner.\n',
          nextAction: 'Share the memo with the owner.',
          summaryText: 'Decision memo generated by local provider.',
        },
        {
          artifactContent:
            '# Reviewer Report\n\n## Verdict\n- verdict: pass\n\n## Checks\n- pass: content-sections - Decision memo sections are present.\n\n## Findings\n- No findings. The draft preserves required sections and includes a next action.\n\n## Next Action\n- continue to completion or approval gate\n',
          checks: [
            {
              description: 'Decision memo sections are present.',
              id: 'content-sections',
              passed: true,
            },
          ],
          findings: [],
          summaryText: 'Reviewer accepted the local provider draft.',
          verdict: 'pass',
        },
      ];

      return {
        ok: true,
        status: 200,
        async json() {
          return {
            choices: [
              {
                message: {
                  content: JSON.stringify(outputs[localChatRequestCount - 1]),
                  role: 'assistant',
                },
              },
            ],
            id: `chatcmpl_local_provider_events_${localChatRequestCount}`,
          };
        },
      };
    }

    throw new Error(`Unexpected provider events url: ${url}`);
  };

  const store = createStore({ rootDir: tempRoot });
  const service = createMissionService({ store, rootDir: tempRoot });

  const workspace = service.addWorkspace({
    name: 'provider-events-workspace',
    workspacePath,
  });

  await service.probeProvider('local');

  const stubFailedMission = service.createMission({
    workspaceId: workspace.id,
    mode: 'knowledge',
    deliverableType: 'checklist',
    title: 'Stub failed mission',
    objective: 'Produce one failed stub mission for execution events.',
    constraints: ['force-rubric-fail'],
  });

  const localMission = service.createMission({
    workspaceId: workspace.id,
    mode: 'knowledge',
    deliverableType: 'decision-memo',
    title: 'Local provider events mission',
    objective: 'Validate unified provider events.',
    constraints: [],
  });

  const stubRun = await service.runMission(stubFailedMission.id, {
    provider: 'stub',
    providerSpecified: true,
  });
  const localRun = await service.runMission(localMission.id, {
    provider: 'local',
    providerSpecified: true,
  });

  assert.equal(stubRun.mission.status, 'failed');
  assert.equal(localRun.mission.status, 'completed');
  assert.equal(localChatRequestCount, 4);
} finally {
  globalThis.fetch = originalFetch;

  for (const [key, value] of Object.entries(originalEnv)) {
    if (value === undefined) {
      delete process.env[key];
    } else {
      process.env[key] = value;
    }
  }
}

