import assert from 'node:assert/strict';
import { spawnSync } from 'node:child_process';
import fs from 'node:fs';
import os from 'node:os';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

import { createLocalProvider } from '../src/providers/local-provider.mjs';

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(scriptDir, '..');
const tempRoot = fs.mkdtempSync(path.join(os.tmpdir(), 'personal-ai-agent-local-provider-'));
const workspacePath = path.join(tempRoot, 'workspace');
const cliPath = path.join(repoRoot, 'src', 'cli.mjs');

fs.mkdirSync(workspacePath, { recursive: true });

function runCli({ args, env = {} }) {
  const result = spawnSync(process.execPath, [cliPath, ...args], {
    cwd: repoRoot,
    encoding: 'utf8',
    env: {
      ...process.env,
      ...env,
      LOCAL_PROVIDER_MODEL: env.LOCAL_PROVIDER_MODEL || '',
      PERSONAL_AI_AGENT_ROOT: tempRoot,
    },
  });

  return {
    status: result.status,
    stderr: String(result.stderr || ''),
    stdout: String(result.stdout || ''),
  };
}

const workspaceCreate = runCli({
  args: ['workspace', 'add', workspacePath, '--name', 'local-provider-workspace'],
});
assert.equal(workspaceCreate.status, 0);
const workspace = JSON.parse(workspaceCreate.stdout);

const missionCreate = runCli({
  args: [
    'mission',
    'create',
    '--workspace',
    workspace.id,
    '--mode',
    'knowledge',
    '--deliverable',
    'decision-memo',
    '--title',
    'Local provider env validation',
    '--objective',
    'Verify local provider wiring and configuration handling.',
  ],
});
assert.equal(missionCreate.status, 0);
const mission = JSON.parse(missionCreate.stdout);

const missingModelRun = runCli({
  args: ['mission', 'run', mission.id, '--provider', 'local'],
  env: {
    LOCAL_PROVIDER_MODEL: '',
  },
});

assert.notEqual(missingModelRun.status, 0);
assert.match(missingModelRun.stderr, /LOCAL_PROVIDER_MODEL is required/i);

let capturedRequest = null;
const provider = createLocalProvider({
  rootDir: repoRoot,
  env: {
    LOCAL_PROVIDER_API_KEY: 'local-test-key',
    LOCAL_PROVIDER_BASE_URL: 'http://127.0.0.1:1234/v1',
    LOCAL_PROVIDER_MAX_TOKENS: '1024',
    LOCAL_PROVIDER_MODEL: 'llama3.1-local',
  },
  fetchImpl: async (url, init) => {
    capturedRequest = {
      body: JSON.parse(init.body),
      headers: init.headers,
      method: init.method,
      url,
    };

    return {
      ok: true,
      status: 200,
      async json() {
        return {
          choices: [
            {
              message: {
                content: JSON.stringify({
                  artifactContent:
                    '# Manager Context\n\n## Mission\n- title: Local provider smoke\n\n## Objective\nValidate the adapter.\n\n## Relevant Memory\n- no relevant memory found\n\n## Governance\n- approval likely: no\n- risk reason: none\n',
                  summaryText: 'Manager context generated by mocked local provider.',
                }),
                role: 'assistant',
              },
            },
          ],
          id: 'chatcmpl_local_test',
        };
      },
    };
  },
});

const providerInput = {
  role: 'manager',
  mission: {
    constraints: [],
    deliverableType: 'decision-memo',
    id: 'mission_test',
    mode: 'knowledge',
    objective: 'Validate the adapter.',
    title: 'Local provider smoke',
  },
  workspace: {
    id: 'workspace_test',
    name: 'Smoke Workspace',
    path: workspacePath,
  },
  pack: {
    artifactFileName: 'decision-memo.md',
    artifactTitle: 'Decision Memo',
    plannerGuidance: ['Keep the scope bounded.'],
    requiredSections: ['Context', 'Options', 'Recommendation', 'Why This Path', 'Next Action'],
    reviewRules: [],
    riskProfile: {
      actionKind: 'document-draft',
      reason: 'none',
      requiresApproval: false,
      title: 'No approval required',
    },
  },
  memoryEntries: [],
  previousOutputs: {},
};

const rawOutput = await provider.run(providerInput);
const normalizedOutput = provider.normalizeOutput(rawOutput, providerInput);

assert.equal(capturedRequest.method, 'POST');
assert.equal(capturedRequest.url, 'http://127.0.0.1:1234/v1/chat/completions');
assert.equal(capturedRequest.body.model, 'llama3.1-local');
assert.equal(capturedRequest.body.max_tokens, 1024);
assert.equal(capturedRequest.body.temperature, 0);
assert.equal(capturedRequest.body.messages[0].role, 'system');
assert.equal(capturedRequest.body.messages[1].role, 'user');
assert.match(capturedRequest.body.messages[1].content, /Structured Output Contract/);
assert.equal(capturedRequest.headers.Authorization, 'Bearer local-test-key');
assert.equal(normalizedOutput.type, 'manager');
assert.equal(normalizedOutput.artifactFileName, 'manager-context.md');
assert.equal(normalizedOutput.artifactTitle, 'Manager Context');
assert.equal(normalizedOutput.summaryText, 'Manager context generated by mocked local provider.');
assert.match(normalizedOutput.artifactContent, /# Manager Context/);

console.log(
  JSON.stringify(
    {
      ok: true,
      mode: 'local-provider',
      requestModel: capturedRequest.body.model,
      validatedMissingModelPath: true,
    },
    null,
    2,
  ),
);
