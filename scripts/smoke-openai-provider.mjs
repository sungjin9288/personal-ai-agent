const timeoutProvider = createOpenAIProvider({
  rootDir: repoRoot,
  env: {
    OPENAI_API_KEY: 'test-key',
    OPENAI_BASE_URL: 'https://api.openai.test/v1',
    OPENAI_MODEL: 'gpt-5.2',
    OPENAI_RUN_TIMEOUT_MS: '10',
  },
  fetchImpl: (url, init = {}) =>
    new Promise((resolve, reject) => {
      const signal = init.signal;
      const abortHandler = () => {
        reject(new DOMException('This operation was aborted', 'AbortError'));
      };

      if (signal) {
        if (signal.aborted) {
          abortHandler();
          return;
        }
        signal.addEventListener('abort', abortHandler, { once: true });
      }

      delay(25)
        .then(() => {
          if (signal) {
            signal.removeEventListener('abort', abortHandler);
          }
          resolve({
            ok: true,
            status: 200,
            async json() {
              return {
                id: 'resp_timeout_test',
                output_text: JSON.stringify({
                  artifactContent: '# Timed Out',
                  summaryText: 'Should not complete before timeout',
                }),
              };
            },
          });
        })
        .catch(reject);
    }),
});

await assert.rejects(
  () => timeoutProvider.run(providerInput),
  (error) => {
    assert.equal(error.name, 'ProviderFailureError');
    assert.equal(error.failure.failureKind, 'timeout');
    assert.equal(error.failure.timedOut, true);
    return true;
  },
);

