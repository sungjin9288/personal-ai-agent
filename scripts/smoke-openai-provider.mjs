import assert from 'node:assert/strict';
import { spawnSync } from 'node:child_process';
import fs from 'node:fs';
import os from 'node:os';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

import { createOpenAIProvider } from '../src/providers/openai-provider.mjs';

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(scriptDir, '..');
const tempRoot = fs.mkdtempSync(path.join(os.tmpdir(), 'personal-ai-agent-openai-provider-'));
const workspacePath = path.join(tempRoot, 'workspace');
const cliPath = path.join(repoRoot, 'src', 'cli.mjs');

fs.mkdirSync(workspacePath, { recursive: true });

function runCli({ args, env = {} }) {
  const result = spawnSync(process.execPath, [cliPath, ...args], {
    cwd: repoRoot,
    encoding: 'utf8',
    env: {
      ...process.env,
      ...env,
      OPENAI_API_KEY: env.OPENAI_API_KEY || '',
      PERSONAL_AI_AGENT_ROOT: tempRoot,
    },
  });

  return {
    status: result.status,
    stderr: String(result.stderr || ''),
    stdout: String(result.stdout || ''),
  };
}

const workspaceCreate = runCli({
  args: ['workspace', 'add', workspacePath, '--name', 'openai-provider-workspace'],
});
assert.equal(workspaceCreate.status, 0);
const workspace = JSON.parse(workspaceCreate.stdout);

const missionCreate = runCli({
  args: [
    'mission',
    'create',
    '--workspace',
    workspace.id,
    '--mode',
    'knowledge',
    '--deliverable',
    'decision-memo',
    '--title',
    'OpenAI provider env validation',
    '--objective',
    'Verify provider wiring and configuration handling.',
  ],
});
assert.equal(missionCreate.status, 0);
const mission = JSON.parse(missionCreate.stdout);

const missingKeyRun = runCli({
  args: ['mission', 'run', mission.id, '--provider', 'openai'],
  env: {
    OPENAI_API_KEY: '',
  },
});

assert.notEqual(missingKeyRun.status, 0);
assert.match(missingKeyRun.stderr, /OPENAI_API_KEY is required/i);

let capturedRequest = null;
const provider = createOpenAIProvider({
  rootDir: repoRoot,
  env: {
    OPENAI_API_KEY: 'test-key',
    OPENAI_BASE_URL: 'https://api.openai.test/v1',
    OPENAI_MODEL: 'gpt-5.2',
  },
  fetchImpl: async (url, init) => {
    capturedRequest = {
      body: JSON.parse(init.body),
      headers: init.headers,
      method: init.method,
      url,
    };

    return {
      ok: true,
      status: 200,
      async json() {
        return {
          id: 'resp_test',
          output_text: JSON.stringify({
            artifactContent: '# Manager Context\n\n## Mission\n- title: OpenAI provider smoke\n\n## Objective\nValidate the adapter.\n\n## Relevant Memory\n- no relevant memory found\n\n## Governance\n- approval likely: no\n- risk reason: none\n',
            summaryText: 'Manager context generated by mocked OpenAI provider.',
          }),
        };
      },
    };
  },
});

const providerInput = {
  role: 'manager',
  mission: {
    constraints: [],
    deliverableType: 'decision-memo',
    id: 'mission_test',
    mode: 'knowledge',
    objective: 'Validate the adapter.',
    title: 'OpenAI provider smoke',
  },
  workspace: {
    id: 'workspace_test',
    name: 'Smoke Workspace',
    path: workspacePath,
  },
  pack: {
    artifactFileName: 'decision-memo.md',
    artifactTitle: 'Decision Memo',
    plannerGuidance: ['Keep the scope bounded.'],
    requiredSections: ['Context', 'Options', 'Recommendation', 'Why This Path', 'Next Action'],
    reviewRules: [],
    riskProfile: {
      actionKind: 'document-draft',
      reason: 'none',
      requiresApproval: false,
      title: 'No approval required',
    },
  },
  memoryEntries: [],
  previousOutputs: {},
};

const rawOutput = await provider.run(providerInput);
const normalizedOutput = provider.normalizeOutput(rawOutput, providerInput);

assert.equal(capturedRequest.method, 'POST');
assert.equal(capturedRequest.url, 'https://api.openai.test/v1/responses');
assert.equal(capturedRequest.body.model, 'gpt-5.2');
assert.match(capturedRequest.headers.Authorization, /^Bearer test-key$/);
assert.match(capturedRequest.body.input, /Structured Output Contract/);
assert.equal(normalizedOutput.type, 'manager');
assert.equal(normalizedOutput.artifactFileName, 'manager-context.md');
assert.equal(normalizedOutput.artifactTitle, 'Manager Context');
assert.equal(normalizedOutput.summaryText, 'Manager context generated by mocked OpenAI provider.');
assert.match(normalizedOutput.artifactContent, /# Manager Context/);

console.log(
  JSON.stringify(
    {
      ok: true,
      mode: 'openai-provider',
      requestModel: capturedRequest.body.model,
      validatedMissingKeyPath: true,
    },
    null,
    2,
  ),
);
